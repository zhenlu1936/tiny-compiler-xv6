// 这个疑似会导致爆栈
int main()
{
	int a,b,c,d,e,i,j,k;
	input a;
	input b;
	input c;
	j = 5;

	while(j>0)
	{
		output j;

		i = 9;
		while(i>0)
		{
			output i;
			d=a+b*c-(a+c)/b+9;
			e=a+b*c-(c-a)/b+9;
			i = i - 1;
		}

		j = j - 1;
		output " ";
	}

	output "\n";
	output d;
	output " ";
	output e;
	output "\n";	
}
